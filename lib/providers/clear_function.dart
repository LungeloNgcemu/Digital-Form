// Define a provider class to hold an instance of MyFunctionality
class ProviderClear1 extends ChangeNotifier {
  void Function()? clearFunction;

  ProviderClear1({
    this.clearFunction,
  });

  void setMyFunction({required void Function() newFunction}) {
    clearFunction = newFunction;
    notifyListeners();
  }
}

